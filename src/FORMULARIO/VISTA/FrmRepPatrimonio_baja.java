    private void abrir_formulario() {
        this.setTitle(nombre_formulario);
        evetbl.centrar_formulario_internalframa(this);
        evefec.setFechaDCSistema(dcfecDesde);
        evefec.setFechaDCSistema(dcfecHasta);
        actualizar_tabla_producto();
    }
    private void actualizar_tabla_producto(){
        String filtro_fecha = evefec.getFiltroSql_desde_hasta_campo(dcfecDesde, dcfecHasta, "pci.fecha_creado");
        DAOpb.reporte_actualizar_tabla_patrimonio_baja(conn, tblpatrimonio_baja, filtro_fecha);
    }
    private void imprimir_producto_movimiento(){
        String filtro_fecha = evefec.getFiltroSql_desde_hasta_campo(dcfecDesde, dcfecHasta, "pci.fecha_creado");
        DAOpb.imprimir_patrimonio_baja(conn, filtro_fecha);
    }
